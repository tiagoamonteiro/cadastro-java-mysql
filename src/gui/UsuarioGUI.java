    public void preencherJTable(){   
        DefaultTableModel modeloTable = (DefaultTableModel) jTable1.getModel();
        Object rowData[] = new Object[5];
        
        UsuarioDAO dao = new UsuarioDAO();
        
        //Percorre todo ArrayList 
        for(Usuario u : dao.getUsuario()){
            //Adiciona os dados no JTable
            rowData[0] = u.getId();
            rowData[1] = u.getNome();
            rowData[2] = u.getCpf();
            rowData[3] = u.getEmail();
            rowData[4] = u.getTelefone();
            modeloTable.addRow(rowData);
        }
    }

